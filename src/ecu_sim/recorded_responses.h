// Default ECU-simulator table (synthetic demo drive).
// Regenerate from a real capture with:
//   python scripts/log_to_header.py capture.log
// This lets the ecu-sim firmware build and run before you've recorded anything.

#include <stdint.h>

struct SimFrame {
  uint32_t id;
  uint8_t len;
  uint8_t data[8];
};

// One response = a contiguous run of SimFrames (multiple = ISO-TP multi-frame).
struct SimResponse {
  uint16_t frameStart;
  uint8_t frameCount;
};

// One request key = a contiguous run of SimResponses (the recorded timeline).
struct SimKey {
  uint8_t mode;
  uint8_t pid;
  uint8_t hasPid;
  uint16_t respStart;
  uint16_t respCount;
};

